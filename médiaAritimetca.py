def media(num1, num2):
    return num1 + num2 / 2
    

print(9*"*"+ " Insira a nota do primeiro aluno " + 9*"*")
nota1_aluno1 = float(input('Digite a 1º nota: '))
nota2_aluno1 = float(input('Digite a 2º nota: '))

total1 = media(nota1_aluno1, nota2_aluno1)
print(f"\nA sua media é: {total1:.2f}")

